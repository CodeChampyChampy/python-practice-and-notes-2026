#Exercise 5 — Basic arithmetic bundle
def calculate_two_numbers_basic_operation(num1, num2):
    addition = num1 + num2
    subtraction = num1 - num2
    multiplication = num1 * num2
    division = num1 / num2
    return (addition, subtraction, multiplication, division)

#Exercise 6 — Floor division
def calculate_floor_division_two_numbers(num1, num2):
    return num1 // num2

#Exercise 7 — Modulo operation
def modulo(num1, num2):
    return num1 % num2

#Exercise 8 —  Exponentiation
def power(base, exponent):
    return base ** exponent
