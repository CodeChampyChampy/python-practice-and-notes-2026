#Exercise 9 — Convert string to int
def convert_string_to_int(myString: str)-> int:
    return int(myString)

#Exercise 10 — Convert string to float
def convert_string_to_float(myString: str)-> float:
    return float(myString)

#Exercise 11 — Convert number to float
def convert_number_to_float(num)-> float:
    return float(num)

#Exercise 12 — Convert number to int
def convert_number_to_int(num)-> int:
    return int(num)

print(convert_string_to_int("24"))
print(convert_string_to_float("2.4"))
print(convert_number_to_float(24))
print(convert_number_to_int(24))

