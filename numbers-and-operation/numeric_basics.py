#Exercise 1 — Sum two numbers
def sum_two_numbers(num1:float, num2:float):
    return num1 + num2

#Exercise 2 — Multiply two numbers
def multiply_two_numbers(num1, num2):
    return num1 * num2

#Exercise 3 — Divide two numbers
def divide_two_numbers(num1, num2):
    return num1 / num2

#Exercise 4 — Mixed numeric addition
def add_int_and_float(num1:int, num2:float):
    return num1 + num2
